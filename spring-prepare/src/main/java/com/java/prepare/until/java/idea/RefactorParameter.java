package com.java.prepare.until.java.idea;

public class RefactorParameter {
    private final String a;
    private final String b;
    private final String c;

    public RefactorParameter(String a, String b, String c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }
}
